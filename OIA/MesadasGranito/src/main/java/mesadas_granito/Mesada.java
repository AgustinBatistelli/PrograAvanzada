package mesadas_granito;

public class Mesada {
	
	private int ancho;
	private int largo;

	public Mesada(Integer ancho, Integer largo) {
		this.setAncho(ancho);
		this.setLargo(largo);
	}
	
	public int getAncho() {
		return ancho;
	}
	public void setAncho(int ancho) {
		this.ancho = ancho;
	}
	public int getLargo() {
		return largo;
	}
	public void setLargo(int largo) {
		this.largo = largo;
	}


	
	
}
